package com.mahfoos;

public class PrimitiveType {

    public static void main(String[] args) {
        // Storing simple values
        /*
        Type        Bytes       Range
        byte          1         [-128,127]
        short         2         [-32K,32K]
        int           4         [-2B,2B]
        long          8
        float         4
        double        8
        char          2         A,B,C..
        boolean       1         true/false
         */

        byte age = 30;
        long viewsCount = 3_123_456_789L; // if we initialised a long need to put end l
        float price = 10.99F;
        char letter = 'A';
        boolean isEligible = true;

    }
}
